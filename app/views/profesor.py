class Profesor():
    def __init__(self, nombre = "", filas = "", columnas = "", imagen = ""):
        self.nombre = nombre
        self.filas = filas
        self.columnas = columnas
        self.imagen = imagen
    
    def getNombre():
        return self.nombre

    def getFilas():
        return self.filas
    
    def getColumnas():
        return self.columnas

    def getImagen():
        return self.imagen

    def setNombre(self, nombre):
        self.nombre = nombre

    def setFilas(self, filas):
        self.filas = filas
    
    def setColumnas(self, columnas):
        self.columnas = columnas

    def setImagen(self, imagen):
        self.imagen = imagen